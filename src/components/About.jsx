import gsap from "gsap";
import { useGSAP } from "@gsap/react";
import { ScrollTrigger } from "gsap/all";

import AnimatedTitle from "./AnimatedTitle";

gsap.registerPlugin(ScrollTrigger);

const About = () => {
  useGSAP(() => {
    const clipAnimation = gsap.timeline({
      scrollTrigger: {
        trigger: "#clip",
        start: "center center",
        end: "+=800 center",
        scrub: 0.5,
        pin: true,
        pinSpacing: true,
      },
    });

    clipAnimation.to(".mask-clip-path", {
      width: "100vw",
      height: "100vh",
      borderRadius: 0,
    });
  });

  return (
    <div id="about" className="min-h-screen w-screen">
      <div className="relative mb-8 mt-36 flex flex-col items-center gap-5">
        <p className="font-general text-sm uppercase md:text-[10px]">
          A Little About Me
        </p>

        <AnimatedTitle
          title="Crafting Digital Exp<b>e</b>riences That <br /> C<b>o</b>nnect & Inspire"
          containerClass="mt-5 !text-black text-center"
        />

        <div className="about-subtext">
          <p>Hi, I’m Mubasheer — a developer passionate about building seamless, responsive, and beautiful web experiences.</p>
          <p className="text-gray-500">
            I combine design thinking and full-stack development to turn ideas into scalable products, using modern tools like React, Next.js, Tailwind, MongoDB, and more.
          </p>
        </div>
      </div>

      <div className="h-dvh w-screen" id="clip">
        <div className="mask-clip-path about-image">
          <img
            src="img/about.webp"
            alt="Background"
            className="absolute left-0 top-0 size-full object-cover"
          />
        </div>
      </div>
    </div>
  );
};

export default About;
